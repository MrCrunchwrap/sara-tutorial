/* This section is short and sweet. The console is built in to the
	Javascript engine in most modern browsers (read: not IE8). It allows
	developers to have a place to log the output of their code, mostly
	for debugging purposes
*/

console.log(3);

console.log("A string");

var myVar = 15;

console.log(myVar);

myVar = 30;

console.log(myVar);

// This syntax is also useful:

console.log("myVar value: ", myVar);

// Operations within the call to console.log are fine

console.log(myVar + 15);
